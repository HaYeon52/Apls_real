/**
 * 관심분야 배열을 케이스 ID로 변환하는 함수
 *
 * @param interestAreas - 사용자가 선택한 관심분야 배열 (최대 2개, 순서 유지)
 * @returns 해당하는 케이스 ID
 */
export function getCaseId(interestAreas: string[]): string {
  // "계획 없음" 또는 관심분야가 없는 경우
  if (
    interestAreas.length === 0 ||
    interestAreas.includes("계획 없음")
  ) {
    return "no-interest";
  }

  // 관심분야명을 정규화 (InterestAreaStep의 실제 값)
  const normalizeInterest = (interest: string): string => {
    const mapping: Record<string, string> = {
      "공정 (생산, 품질)": "manufacturing",
      "물류/SCM": "logistics",
      데이터: "data",
      금융: "finance",
      "컨설팅/기획": "consulting",
    };
    return mapping[interest] || interest;
  };

  // 순서를 유지하면서 정규화
  const normalized = interestAreas.map(normalizeInterest);

  // 단일 관심분야
  if (normalized.length === 1) {
    return normalized[0];
  }

  // 조합 관심분야 (2개) - 순서 유지!
  if (normalized.length === 2) {
    return `${normalized[0]}-${normalized[1]}`;
  }

  // 예외 상황 (3개 이상은 없어야 함)
  console.warn(
    "⚠️ 관심분야가 2개를 초과했습니다:",
    interestAreas,
  );
  return "no-interest";
}