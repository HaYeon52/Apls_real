// 케이스 매칭 함수
export function matchCase(interestArea: string[]): string {
  if (interestArea.length === 0) {
    return "none";
  }
  
  if (interestArea.length === 1) {
    return interestArea[0];
  }
  
  // 2개 선택한 경우
  // 한글 정렬: 공정 < 금융 < 데이터 < 물류 < 컨설팅
  // 따라서 정렬하면: 공정+금융, 공정+데이터, 공정+물류, 공정+컨설팅, ...
  const sorted = [...interestArea].sort();
  const key = sorted.join("+");
  
  // 매칭되는 케이스가 없으면 첫 번째 선택만 사용
  if (!(key in recommendationCases)) {
    return sorted[0];
  }
  
  return key;
}